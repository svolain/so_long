#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*head;

	head = (t_list *)malloc(sizeof(t_list));
	if (head == 0)
		return (0);
	head->content = content;
	head->next = NULL;
	return (head);
}
