#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dup;
	int		strlen;
	int		i;

	i = 0;
	strlen = ft_strlen((char *)s1);
	dup = (char *)malloc(sizeof(char) * (strlen + 1));
	if (dup == '\0')
		return (0);
	while (s1[i] != '\0')
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
